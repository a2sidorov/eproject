package dev.a2.estore.model;

/**
 * This model represents the main data about the company that sells goods via this application.
 *
 * @author Andrei Sidorov
 */

public class CompanyInfo {

    /**
     * The name of a company.
     */
    private String name;

    /**
     * The email of a company.
     */
    private String email;

    /**
     * The web site of a company.
     */
    private String website;

    /**
     * The phone number of a company.
     */
    private String phoneNumber;

    /**
     * The fax number of a company.
     */
    private String faxNumber;

    /**
     * The country of a company.
     */
    private String country;

    /**
     * The city of a company.
     */
    private String city;

    /**
     * The postal code of a company.
     */
    private String postalCode;

    /**
     * The name of a street.
     */
    private String street;

    /**
     * The number of a house.
     */
    private String house;

    /**
     * The number of an apartment.
     */
    private String apartment;

    /* Getters and setters */

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getWebsite() {
        return website;
    }

    public void setWebsite(String website) {
        this.website = website;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getFaxNumber() {
        return faxNumber;
    }

    public void setFaxNumber(String faxNumber) {
        this.faxNumber = faxNumber;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getHouse() {
        return house;
    }

    public void setHouse(String house) {
        this.house = house;
    }

    public String getApartment() {
        return apartment;
    }

    public void setApartment(String apartment) {
        this.apartment = apartment;
    }
}
